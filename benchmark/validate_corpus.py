#!/usr/bin/env python3
"""Corpus validator: run every entry's code through vm.exe --lint and flag
parse failures, runtime errors, or other syntactic issues.

Why: the bulk corpus (~2046 historical entries) was generated by Sonnet/Opus
months ago with various prompt iterations. We never validated that each
entry parses, let alone runs. Some may be broken — and the model trains
on broken examples just as readily as good ones.

This pass classifies each entry into:
  ok           - parses with --lint, no obvious red flags
  parse_error  - vm.exe --lint rejected it
  empty        - empty assistant message
  suspicious   - parses but contains anti-patterns (deprecated names, etc.)

Outputs:
  corpus_validation_report.txt
  training_corpus.cleaned.jsonl  (only `ok` entries; the rest dropped or fixed)
"""
import json
import os
import re
import subprocess
import tempfile
from collections import Counter
from pathlib import Path

REPO = Path(__file__).resolve().parent.parent
SIGIL = str(REPO / "interpreter" / "_build" / "default" / "vm.exe")
CORPUS_IN = REPO / "benchmark" / "training_corpus.jsonl"
CORPUS_OUT = REPO / "benchmark" / "training_corpus.cleaned.jsonl"
REPORT_OUT = REPO / "benchmark" / "corpus_validation_report.txt"

# Anti-patterns that flag a SUSPICIOUS entry. These are not always wrong, but
# they're shapes that often confuse the model when it learns them.
ANTIPATTERNS = [
    # 2-statement-then-without-else (the if-Lisp-form trap). Only flag when both
    # statements look like (set ...) — that's the most common buggy intent.
    (re.compile(r'\(if\s+\([^)]+\)\s+\(set\s+\w+[^)]*\)\s+\(set\s+\w+[^)]*\)\s*\)'),
     "if-Lisp-trap: 2-set then without explicit (else)"),
    # Calling argv_int with a non-int-literal is fine; but calling arg_int with
    # a string literal is the foot-gun (it's been refactored to error explicitly).
    (re.compile(r'\(arg_int\s+"'),
     "arg_int called with string (should be parse_int)"),
    # Reaching for nonexistent functions
    (re.compile(r'\(string_length\s'),
     "string_length is not a builtin (use len)"),
    (re.compile(r'\(second\s'),
     "second is not a builtin"),
    (re.compile(r'\(third\s'),
     "third is not a builtin"),
]


def lint(code: str) -> tuple[bool, str]:
    """Run vm.exe --lint and report success/failure."""
    f = tempfile.NamedTemporaryFile(mode="w", suffix=".sigil", delete=False)
    f.write(code); f.close()
    try:
        r = subprocess.run([SIGIL, "--lint", f.name],
                          capture_output=True, text=True, timeout=10)
        if r.returncode == 0:
            return True, ""
        # Extract the first useful line of error
        msg = (r.stderr.strip() or r.stdout.strip()).splitlines()
        return False, msg[0] if msg else "unknown error"
    except subprocess.TimeoutExpired:
        return False, "lint timeout"
    except Exception as e:
        return False, f"lint exception: {e}"
    finally:
        os.unlink(f.name)


def main():
    n = 0
    classes = Counter()
    by_error = Counter()
    by_antipattern = Counter()
    cleaned = []
    report = []

    with CORPUS_IN.open() as fin:
        entries = [json.loads(l) for l in fin if l.strip()]

    print(f"Validating {len(entries)} entries...")
    for i, entry in enumerate(entries):
        n += 1
        msgs = entry["messages"]
        asst = next((m for m in msgs if m["role"] == "assistant"), None)
        user = next((m for m in msgs if m["role"] == "user"), None)
        if not asst or not asst["content"].strip():
            classes["empty"] += 1
            continue
        code = asst["content"]

        # Lint
        ok, err = lint(code)
        if not ok:
            classes["parse_error"] += 1
            by_error[err[:80]] += 1
            user_q = user["content"][:60] if user else ""
            report.append(f"[{i}] PARSE_ERROR: {err[:120]}\n  user: {user_q!r}\n  code (first 200): {code[:200]}\n")
            continue

        # Anti-pattern scan
        flags = []
        for pat, name in ANTIPATTERNS:
            if pat.search(code):
                flags.append(name)
                by_antipattern[name] += 1
        if flags:
            classes["suspicious"] += 1
            user_q = user["content"][:60] if user else ""
            report.append(f"[{i}] SUSPICIOUS: {flags}\n  user: {user_q!r}\n")

        # ok or suspicious: keep
        cleaned.append(entry)

        if (i + 1) % 200 == 0:
            print(f"  {i+1}/{len(entries)} processed; classes={dict(classes)}")

    # Final breakdown
    classes["ok_clean"] = len(cleaned) - classes["suspicious"]

    print()
    print(f"=== Final classification (n={n}) ===")
    for k, v in classes.items():
        print(f"  {k:14s} {v:>5}  ({100*v/n:.1f}%)")
    print()
    if by_error:
        print("Top parse errors:")
        for err, cnt in by_error.most_common(10):
            print(f"  {cnt:>4} | {err}")
    print()
    if by_antipattern:
        print("Anti-pattern hits:")
        for ap, cnt in by_antipattern.most_common():
            print(f"  {cnt:>4} | {ap}")

    REPORT_OUT.write_text("\n".join(report))
    CORPUS_OUT.write_text("\n".join(json.dumps(e, ensure_ascii=False) for e in cleaned) + "\n")
    print(f"\nReport: {REPORT_OUT}")
    print(f"Cleaned corpus: {CORPUS_OUT} ({len(cleaned)} entries kept, {n - len(cleaned)} dropped)")


if __name__ == "__main__":
    main()
